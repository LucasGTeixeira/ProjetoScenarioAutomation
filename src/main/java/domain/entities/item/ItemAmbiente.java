package domain.entities.item;

public abstract class ItemAmbiente {
    private String nome;

    public ItemAmbiente(String nome) {
        this.nome = nome;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    @Override
    public String toString() {
        return nome;
    }
}
